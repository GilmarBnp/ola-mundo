package Módulo3;

public class BuscaBináriaTeoria {
	/*
 	- Também chamada de pesquisa binária
 	- É um algoritmo de busca em listas que utiliza a ideia da divisão e conquista
 	- Para ele funcionar, é preciso que a lista esteja previamente ordenada
 	- Ele inicializa verificando se o elemento buscado é maior ou menor que o elemento do meio da lista
 	- Se o elemento do meio for igual ao buscado o algoritmo é encerrado
 	- Se o elemento do meio for menor que o buscado, o algoritmo realiza o mesmo procedimento novamente porém utilizando somente a primeira metade da lista
 	- Se o elemento buscado for maior que o elemento do meio,  o mesmo ocorre só que com a segunda metade da lista 
 	- Esse processo é então repetido até que seja encontrado o elemento desejado
 	- Em uma lista ordenada, esse algoritmo é mais eficiente que a busca linear
 	- Sempre vai procurando o elemento cortando o meio, caso não esteja, corta outro meio novamente, e vai cortando o meio até achar o elemento buscado
	 */
}
